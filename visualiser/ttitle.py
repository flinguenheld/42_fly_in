class TTitle(Static, Anim):
    _current = reactive(0)

    def __init__(self, tempo: float = 0.06, classes: str = "") -> None:
        Static.__init__(self, classes=classes)
        Anim.__init__(self)
        self._tempo = tempo
        self._titles = ["Abcde", "aBcde", "abCde", "abcDe", "abcdE"]

    def render(self) -> RenderResult:
        return f"{self._titles[self._current]}"

    # ########################################################################
    # ######################################################## ANIMATION #####
    @override
    async def _anim_run(self) -> None:
        while True:
            for index in range(0, len(self._titles)):
                self._current = index
                await asyncio.sleep(self._tempo)

            for index in reversed(range(0, len(self._titles))):
                self._current = index
                await asyncio.sleep(self._tempo)

            self._current = 0
            await asyncio.sleep(random.randint(2, 10))


